package com.ride.rideapp.models;

import org.springframework.data.annotation.Id;

public abstract class GlobalArea {
    @Id
    int id;
    String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
